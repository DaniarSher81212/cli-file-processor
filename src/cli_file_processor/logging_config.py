"""
Настройка логирования приложения.

Логирование — это запись того, что происходит внутри программы.
В отличие от typer.echo() (вывод для пользователя),
логи предназначены для разработчика и диагностики.
"""

# logging — стандартная библиотека Python для логирования.
# Не нужно устанавливать через pip — она уже есть в Python.
import logging


def setup_logging(verbose: bool = False) -> None:
    """
    Настраивает логирование для всего приложения.

    Аргументы:
        verbose — если True, показывает DEBUG-сообщения (подробный режим).
                  если False, показывает только INFO и выше.
    """

    # Выбираем уровень логирования в зависимости от флага --verbose.
    # logging.DEBUG = 10, logging.INFO = 20 — это просто числа.
    # Сообщения с уровнем НИЖЕ выбранного будут проигнорированы.
    level = logging.DEBUG if verbose else logging.INFO

    # Формат каждой строки лога.
    # %(levelname)-8s — уровень, выровненный по 8 символов: "DEBUG   ", "INFO    "
    # %(name)s        — имя логгера (обычно имя модуля, например "core.scanner")
    # %(message)s     — само сообщение
    log_format = "%(levelname)-8s %(name)s: %(message)s"

    # logging.basicConfig() — настраиваем корневой (root) логгер.
    # Все логгеры в приложении наследуют его настройки.
    # level  — минимальный уровень для показа сообщений
    # format — шаблон строки лога
    # force=True — принудительно перенастраивает, даже если basicConfig уже вызывался.
    #              Без этого повторный вызов basicConfig() молча игнорируется.
    #              Важно в тестах: каждый тест запускает команду заново в одном процессе.
    logging.basicConfig(level=level, format=log_format, force=True)
